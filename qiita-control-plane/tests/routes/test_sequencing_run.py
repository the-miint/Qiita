async def test_create_sequencing_run_wet_lab_admin_minimal(ctx):
    # Wet_lab_admin caller posts the minimum-required body; the route
    # mints a sequencing_run row and returns its idx.
    resp = await _post_run(
        ctx["wet"],
        ctx,
        instrument_run_id=unique_instrument_id("WET"),
        platform="illumina",
    )
    assert resp.status_code == 201, resp.text
    rj = resp.json()
    expected = {
        # Auto-generated by the DB; copy actual into expected so the
        # equality confirms field presence without pinning the idx value.
        "sequencing_run_idx": rj["sequencing_run_idx"],
    }
    assert rj == expected

    # Verify the row landed with the caller's principal_idx as creator and
    # the platform persisted as the enum value.
    row = await ctx["pool"].fetchrow(
        "SELECT instrument_run_id, platform, created_by_idx,"
        " instrument_model, instrument_serial, run_performed_at, extra_metadata"
        " FROM qiita.sequencing_run WHERE idx = $1",
        rj["sequencing_run_idx"],
    )
    actual = dict(row)
    expected = {
        # Auto-generated by the test helper; copy actual into expected so
        # the equality confirms field presence without pinning the value.
        "instrument_run_id": actual["instrument_run_id"],
        "platform": "illumina",
        "created_by_idx": ctx["wet_session"]["principal_idx"],
        "instrument_model": None,
        "instrument_serial": None,
        "run_performed_at": None,
        "extra_metadata": None,
    }
    assert actual == expected


async def test_create_sequencing_run_system_admin_all_fields(ctx):
    # System_admin caller posts every settable column; the route persists
    # all of them.
    instrument_run_id = unique_instrument_id("ADM")
    resp = await _post_run(
        ctx["admin"],
        ctx,
        instrument_run_id=instrument_run_id,
        platform="pacbio_smrt",
        instrument_model="NovaSeq X+",
        instrument_serial="LH00123",
        run_performed_at="2026-04-01T12:00:00Z",
        extra_metadata={"lane_count": 2, "flowcell": "FC-A"},
    )
    assert resp.status_code == 201, resp.text

    row = await ctx["pool"].fetchrow(
        "SELECT instrument_run_id, platform, instrument_model, instrument_serial,"
        " run_performed_at, extra_metadata"
        " FROM qiita.sequencing_run WHERE idx = $1",
        resp.json()["sequencing_run_idx"],
    )
    # asyncpg returns JSONB as a JSON string by default; decode in-place so
    # the dict equality compares structured payloads.
    actual = dict(row)
    actual["extra_metadata"] = json.loads(actual["extra_metadata"])
    expected = {
        "instrument_run_id": instrument_run_id,
        "platform": "pacbio_smrt",
        "instrument_model": "NovaSeq X+",
        "instrument_serial": "LH00123",
        # TIMESTAMPTZ comes back as a tz-aware datetime; copy actual since
        # this assertion only verifies the column was populated.
        "run_performed_at": actual["run_performed_at"],
        "extra_metadata": {"flowcell": "FC-A", "lane_count": 2},
    }
    assert actual == expected


