async def test_create_sequencing_run_missing_required_field_422(ctx):
    # Pydantic validation rejects {} because instrument_run_id and platform
    # are both required.
    resp = await ctx["wet"].post(_ROUTE, json={})
    assert resp.status_code == 422
    missing_locs = {tuple(err["loc"]) for err in resp.json()["detail"]}
    assert ("body", "instrument_run_id") in missing_locs
    assert ("body", "platform") in missing_locs


async def test_create_sequencing_run_invalid_platform_422(ctx):
    # The Platform StrEnum rejects values outside the closed set.
    resp = await ctx["wet"].post(
        _ROUTE,
        json={
            "instrument_run_id": unique_instrument_id("BADP"),
            "platform": "nanopore",
        },
    )
    assert resp.status_code == 422


async def test_create_sequencing_run_empty_instrument_run_id_422(ctx):
    # Pydantic min_length=1 rejects an empty string.
    resp = await ctx["wet"].post(
        _ROUTE,
        json={"instrument_run_id": "", "platform": "illumina"},
    )
    assert resp.status_code == 422


async def test_create_sequencing_run_extra_field_422(ctx):
    # The request model carries extra="forbid"; an unknown field is rejected
    # at the Pydantic boundary rather than silently dropped.
    resp = await ctx["wet"].post(
        _ROUTE,
        json={
            "instrument_run_id": unique_instrument_id("XTRA"),
            "platform": "illumina",
            "not_a_real_column": "x",
        },
    )
    assert resp.status_code == 422


